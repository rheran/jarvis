      <p class="body-text">I have spent my entire life surrounded by cars and people who love driving them.
      <br/><br/>
      I grew up in a house where everything from rally cross to F1 would be on the tv on a sunday, while my dad and his friends wondered in and out of our garage in oil covered overalls, tinkering with various cars he and his friends raced. I was brought up on tales of trips spent on the road exploring wherever the cars could take them
      <br/><br/>
      When we weren't watching motorsport, hanging out in freezing cold garages, or in the pit lane of a racetrack - we were at huge car shows, where enthusiasts would gather to see everything from rare ferrari's to military tanks.
      <br/><br/>
      For us, a family day out was centered around all piling into car, and heading out on the road for as many miles as we could cover before we wanted to kill each other.
      <br/><br/>
      Cars have always been an integral part of my life.</p>
