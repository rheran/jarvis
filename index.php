      <h2 class="sub-heading" style="margin-bottom: 0px; padding-bottom: 0px;">CHANGE MUST COME</h2>
      <p class="body-text" style="margin-top: -80px; padding-top: 0px; margin-bottom: 60px;">what I want to do and why</p>
      <p class="body-text">I am frustrated by the developments I see in the industry of self-driving cars, with so much focus on A to B transport of people - but no investment made into building the cars of the future for the masses of people whose hearts break at the thought of cars as a culture becoming extinct. Tesla, Wayfinder, Uber - these companies are implementing AV’s the easiest way they can, as cold objects used simply as tools to complete a task. Does anyone care about what we stand to lose?
      <br/><br/>
      We are not a niche, we are strong in numbers, on a global scale vary in how we express our automotive creativity but we all stand by the same principles. Passion for the art, freedom to explore, creativity in execution, and customisation to personal taste. AV development is spearheaded by technology companies, it’s become about the most efficient algorithms and the highest safety specifications - but this is only part of the much bigger picture that they’re failing to recognise.</p>


      <!-- 5.5 - what I've learned - why car hacking is a good thing -->

      <p class="body-text">Around 4 months ago, opengarages.org, which houses the car hacker’s handbook. I learned that there are ports in the car that let you connect directly to the computer which controls everything from fuel injection timing to door locking. I had never understood before that these individual electronic controls passed through one central computer - I had seen the inner wirings of cars before but wrongly assumed most controls had independent analogue wiring. I have a strong computing background, I am intrigued by how they work on a deep level and the vast range of possible systems that can be built with them, and this discovery gave me a completely new mental model on how cars worked and what I could do with this computer.
      <br/><br/>
      The more I read into car computers, ECU’s and OBD2 ports and protocols - I could relate my experience with desktop computers to this new system and this gave me a lens that I used to pick out what was within my ability to do. I am not an electrician, I don’t feel comfortable touching any hardwired components - especially when any of them could give you a significant electric shock or damage the vehicle. However, the concept of plugging my own computer into a small port under the steering wheel, specifically designed for diagnostics, was realistic. OBD2 hacking is documented on various places on the internet, but only in small parts. I am yet to discover anyone who has started from nothing and developed a system for car hacking which has been completely documented in a way that can be followed. This was an issue for me, because it meant I had to do lots of individual pieces of research into hardware interfaces and systems which I had never had to use before.</p>



